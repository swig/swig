local v=require("extend_default")

-- %extend before the class definition
local ex = v.Before()
assert(ex.i == -1 and ex.d == -1, "Before constructor 1 failed")
ex = v.Before(10)
assert(ex.i == 10 and ex.d == -1, "Before constructor 2 failed")
ex = v.Before(20, 30)
assert(ex.i == 20 and ex.d == 30, "Before constructor 3 failed")
ex = v.Before()
assert(ex:AddedMethod() == -2, "Before AddedMethod 1 failed")
assert(ex:AddedMethod(-2) == -3, "Before AddedMethod 2 failed")
assert(ex:AddedMethod(-10, -10) == -20, "Before AddedMethod 3 failed")
assert(v.Before.AddedStaticMethod() == -2, "Before AddedStaticMethod 1 failed")
assert(v.Before.AddedStaticMethod(-2) == -3, "Before AddedStaticMethod 2 failed")
assert(v.Before.AddedStaticMethod(-10, -10) == -20, "Before AddedStaticMethod 3 failed")

-- %extend after the class definition
ex = v.After()
assert(ex.i == -1 and ex.d == -1, "After constructor 1 failed")
