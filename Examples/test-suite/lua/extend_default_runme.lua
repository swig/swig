-- Override
local o = v.Override()
assert(o:over() == -1, "override test 1 failed")
assert(o:over(10) == 10*10, "override test 2 failed")
assert(o:ride() == -1, "override test 3 failed")
assert(o:ride(10) == 10, "override test 4 failed")
assert(o:overload() == -10, "override test 5 failed")
assert(o:overload(10) == 10*10, "override test 6 failed")
