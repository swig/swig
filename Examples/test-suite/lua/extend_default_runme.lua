assert(ex:AddedMethod() == -2, "After AddedMethod 1 failed")
assert(ex:AddedMethod(-2) == -3, "After AddedMethod 2 failed")
assert(ex:AddedMethod(-10, -10) == -20, "After AddedMethod 3 failed")
assert(v.After.AddedStaticMethod() == -2, "After AddedStaticMethod 1 failed")
assert(v.After.AddedStaticMethod(-2) == -3, "After AddedStaticMethod 2 failed")
assert(v.After.AddedStaticMethod(-10, -10) == -20, "After AddedStaticMethod 3 failed")

