local v=require("cpp11_delegating_constructors")

local a = v.A()
assert(a.a ==  10)
assert(a.b ==  20)
assert(a.c ==  30)
a = v.A(11)
assert(a.a ==  11)
assert(a.b ==  20)
assert(a.c ==  30)
a = v.A(11, 22)
assert(a.a ==  11)
assert(a.b ==  22)
assert(a.c ==  30)
a = v.A(11, 22, 33)
assert(a.a ==  11)
assert(a.b ==  22)
assert(a.c ==  33)
